i = int(input("奖金为多少万元"))
if i <= 10 :
    p = 0.1 * i
elif 10 < i <= 20 :
    p = 1 + 0.075 * (i - 10)
elif 20 < i <= 40 :
    p = 1 + 0.75 + 0.05 * (i - 20)
elif 40 < i <= 60 :
    p = 1 + 0.75 + 1 + 0.03 * (i - 40)
elif 60 < i <= 100 :
    p = 1 + 0.75 + 1 + 0.6 + 0.015 * (i - 60)
elif 100 < i :
    p = 1 + 0.75 + 1 + 0.6 + 0.6 + 0.01 * (i - 100)
else :
    print("输入金额无效")
print("应发奖金",p,"万元") 